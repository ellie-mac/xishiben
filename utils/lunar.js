/**
 * 判断某天是否宜嫁娶
 */
function isAuspicious(dateStr) {
  return AUSPICIOUS_WEDDING_DATES.has(dateStr)
}

/**
 * 简易农历信息（基于基准点推算）
 * 返回 { lunarMonth, lunarDay, lunarMonthStr, lunarDayStr, ganZhi }
 */
function getLunarInfo(dateStr) {
  // 基准：2025-01-01 = 农历2024年腊月初二
  const BASE_DATE = new Date('2025-01-01')
  const BASE_LUNAR = { year: 2024, month: 12, day: 2 }

  const target = new Date(dateStr)
  const diffDays = Math.round((target - BASE_DATE) / (1000 * 60 * 60 * 24))

  // 简化推算（不考虑闰月，仅供展示参考）
  const lunarYear = BASE_LUNAR.year + Math.floor((BASE_LUNAR.month - 1 + diffDays / 30) / 12)
  const rawMonth = ((BASE_LUNAR.month - 1 + Math.floor(diffDays / 29.5)) % 12) + 1
  const rawDay = ((BASE_LUNAR.day - 1 + diffDays) % 30) + 1

  const yearIndex = (lunarYear - 4) % 60
  const ganZhi = TIAN_GAN[yearIndex % 10] + DI_ZHI[yearIndex % 12]
  const shengXiao = SHI_ER_SHENG_XIAO[(lunarYear - 4) % 12]

  return {
    lunarMonthStr: LUNAR_MONTHS[rawMonth - 1] || '',
    lunarDayStr: LUNAR_DAYS[rawDay - 1] || '',
    ganZhi,
    shengXiao
  }
}

/**
 * 获取某月所有吉日列表
 */
function getMonthAuspicious(year, month) {
  const result = []
  const m = String(month).padStart(2, '0')
  const daysInMonth = new Date(year, month, 0).getDate()
  for (let d = 1; d <= daysInMonth; d++) {
    const day = String(d).padStart(2, '0')
    const dateStr = `${year}-${m}-${day}`
    if (isAuspicious(dateStr)) {
      result.push(dateStr)
    }
  }
  return result
}

